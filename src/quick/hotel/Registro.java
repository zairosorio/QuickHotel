    void buscar() {
        String cc = campo_identificacion.getText();
        if (cc.isEmpty() == true) {
            JOptionPane.showMessageDialog(this, "ingrese IDENTIFICACION para la busqueda");
            campo_identificacion.requestFocus();
            return;
        }

        try {
            Connection con = null;
            String identificacion = campo_identificacion.getText();
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/quick_hotel", "root", "");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from  huesped where IDENTIFICACION='" + identificacion + "'  ");
           
            if (rs.next()) {
                combo_IDENTIDAD.addItem(rs.getString(2));
                campo_pri_nombre.setText(rs.getString(3));
                campo_seg_nombre.setText(rs.getString(4));
                campo_pri_apellido.setText(rs.getString(5));
                campo_seg_apellido.setText(rs.getString(6));
                campo_direccion.setText(rs.getString(7));
                campo_tel.setText(rs.getString(8));
                campo_cel.setText(rs.getString(9));
                ///JOptionPane.showMessageDialog(null, ""+rs.getString(10));
                //Lfecha_naci.setText(rs.getString(10));
                campo_calendario.setDateFormatString(rs.getString(10));
                Lfecha_naci.setText(rs.getString(10));
                campocorreo.setText(rs.getString(11));
                combopais.addItem(rs.getString(12));
                combodpto.addItem(rs.getString(13));
                combociudad.addItem(rs.getString(14));

            } else {
                JOptionPane.showMessageDialog(null, "no se encontro registro con identificacion : " + campo_identificacion.getText());

                int option = JOptionPane.showConfirmDialog(null,
                        "Desea Crear Huesped ?",
                         "Confirmar ",
                        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (option == JOptionPane.YES_OPTION) {
                    campo_pri_nombre.requestFocus();
                    cargar();
                }
            }
        } catch (Exception e) {
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, e);
        }

    }

